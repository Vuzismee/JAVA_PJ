package Model;

public class MonHoc {
    private String maMH;
    private String tenMH;
    private int tclt;
    private int tcth;
    private String maKhoa;

    public MonHoc() {
        super();
    }

    public String getMaMH() {
        return maMH;
    }

    public void setMaMH(String maMH) {
        this.maMH = maMH;
    }

    public String getTenMH() {
        return tenMH;
    }

    public void setTenMH(String tenMH) {
        this.tenMH = tenMH;
    }

    public int getTclt() {
        return tclt;
    }

    public void setTclt(int tclt) {
        this.tclt = tclt;
    }

    public int getTcth() {
        return tcth;
    }

    public void setTcth(int tcth) {
        this.tcth = tcth;
    }

    public String getMaKhoa() {
        return maKhoa;
    }

    public void setMaKhoa(String maKhoa) {
        this.maKhoa = maKhoa;
    }
}